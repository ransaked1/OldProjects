int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	while (*s1 == *s2 && *s1 != '\0' && *s2 != '\0' && n)
	{
		s1++;
		s2++;
		n--;
	}
	if (*s1 > *s2)
		return (1);
	if (*s1 < *s2)
		return (-1);
	if (*s1 == *s2)
		return (0);
	return (0);
}
