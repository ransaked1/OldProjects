int		ft_atoi(char *nb)
{
	int		i;
	int		neg;
	int		num;

	i = 0;
	neg = 1;
	num = 0;
	while (nb[i] == ' ')
		i++;
	while (nb[i] != '\0')
	{
		if (nb[i] == '-')
			neg = -1;
		if (nb[i] >= '0' && nb[i] <= '9')
			num = num * 10 + (nb[i] - '0');
		if (nb[i + 1] < '0' || nb[i + 1] > '9' || nb[i + 1] == '\0')
		{
			num = num * neg;
			return (num);
		}
		i++;
	}
	return (num);
}
