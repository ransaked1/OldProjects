int		ft_str_is_numeric(char *str)
{
	int		i;

	i = 0;
	if (!str[0])
		return (1);
	while (str[i] != '\0')
	{
		if (str[i] >= 1 && str[i] <= 64)
			return (0);
		if (str[i] >= 91 && str[i] <= 96)
			return (0);
		if (str[i] >= 123 && str[i] <= 127)
			return (0);
		i++;
	}
	return (1);
}
